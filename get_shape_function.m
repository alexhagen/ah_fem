function [N] = get_shape_function(xi,eta)
    N = 0.25*[...
        -(1-xi)*(1-eta)*(1+xi+eta) 0 ...
        -(1+xi)*(1-eta)*(1-xi+eta) 0 ...
        -(1+xi)*(1+eta)*(1-xi-eta) 0 ...
        -(1-xi)*(1+eta)*(1+xi-eta) 0 ...
        2*(1-xi)*(1+xi)*(1-eta) 0 ...
        2*(1+xi)*(1-eta)*(1+eta) 0 ...
        2*(1-xi)*(1+xi)*(1+eta) 0 ...
        2*(1-xi)*(1-eta)*(1+eta) 0;...
        0 -(1-xi)*(1-eta)*(1+xi+eta) ...
        0 -(1+xi)*(1-eta)*(1-xi+eta) ...
        0 -(1+xi)*(1+eta)*(1-xi-eta) ...
        0 -(1-xi)*(1+eta)*(1+xi-eta) ...
        0 2*(1-xi)*(1+xi)*(1-eta) ...
        0 2*(1+xi)*(1-eta)*(1+eta) ...
        0 2*(1-xi)*(1+xi)*(1+eta) ...
        0 2*(1-xi)*(1-eta)*(1+eta);];
end